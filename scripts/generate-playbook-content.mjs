function readMarkdown(file) {
  return readFileSync(path.join(repoRoot, file), "utf8")
    .replace(/\r\n/g, "\n")
    .trim();
}

function stripMd(markdown) {
  return markdown
    .replace(/\[[^\]]+\]\([^)]+\)/g, (match) => match.match(/\[([^\]]+)\]/)?.[1] ?? match)
    .replace(/[*_`>#-]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function firstParagraph(markdown) {
  const lines = markdown.split("\n");
  for (const line of lines) {
    const text = line.trim();
    if (!text) continue;
    if (text.startsWith("#")) continue;
    if (text.startsWith("---")) continue;
    if (text.startsWith("- ")) continue;
    if (text.startsWith("> 状态")) continue;
    if (text.startsWith("> 原")) continue;
    return stripMd(text.replace(/^>\s*/, ""));
  }
  return "";
}

function sectionFromPath(file) {
  const markdown = readMarkdown(file);
  const heading = markdown.match(/^#\s+(.+)$/m)?.[1]?.trim() ?? file;
  const no = heading.match(/^(\d+\.\d+)/)?.[1] ?? "";
  const title = heading.replace(/^\d+\.\d+\s*/, "").trim();

  return {
    id: no,
    no,
    title,
    path: file,
    excerpt: firstParagraph(markdown),
    markdown,
  };
}

const generatedChapters = chapters.map((chapter) => {
  const readme = readMarkdown(chapter.readme);
  return {
    ...chapter,
    lede: firstParagraph(readme),
    sections: chapter.sections.map(sectionFromPath),
  };
});

